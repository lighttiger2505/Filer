- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *CellIdentifier = @"Cell";
    
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier];
    if (cell == nil) {
        cell = [[[UITableViewCell alloc] initWithStyle:UITableViewCellStyleDefault reuseIdentifier:CellIdentifier] autorelease];
    }
    
    // Configure the cell...
    if (indexPath.row == TYPE_FILE) {
        cell.textLabel.text = @"ファイル";
    }
    if (indexPath.row == TYPE_FOLDER) {
        cell.textLabel.text = @"フォルダ";
    }
    
    return cell;
}

#pragma mark - Table view delegate

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    int newRow = indexPath.row;
    int oldRow = (selectedIndexPath != nil) ? selectedIndexPath.row : -1;
    
    if (newRow != oldRow) {
        UITableViewCell *newCell = [tableView cellForRowAtIndexPath:indexPath];
        newCell.accessoryType = UITableViewCellAccessoryCheckmark;
        UITableViewCell *oldCell = [tableView cellForRowAtIndexPath:selectedIndexPath];
        oldCell.accessoryType = UITableViewCellAccessoryNone;
        selectedIndexPath = indexPath;
        [self.delegate fileTypeSelector:self selectedFileType:indexPath.row];
     }
    
    [tableView deselectRowAtIndexPath:indexPath animated:YES];
    
}

